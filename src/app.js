        function renderCurrentCard() {
            if (!session) return;
            if (session.queue.length === 0) { showDoneScreen(); return; }
            answerRow.classList.remove('show');
            const w = session.poolMap[session.queue[0]];
            flashcard.classList.add('no-anim'); flashcard.classList.remove('flipped'); void flashcard.offsetWidth;
            wordText.textContent = w.word; meaningText.textContent = w.meaning; void flashcard.offsetWidth; flashcard.classList.remove('no-anim');
            updateProgress();
        }

        function updateProgress() { const m = session.mastered.length, t = session.total; progressFill.style.width = t ? Math.round((m/t)*100)+'%' : '0%'; statQueue.textContent = session.queue.length; statMastered.textContent = m; statTotal.textContent = t; }

        let flipShowTimer = null;
