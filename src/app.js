        /* حضور و تایمر مطالعه (با heartbeat سمت سرور) */
        let presenceInterval=null, onlineUsersInterval=null, currentUsername=null;
        let studyTimerInterval=null, unsyncedSeconds=0, sessionTotalSeconds=0;
        let isStudyActive=true, lastActivityTime=Date.now(); const INACTIVITY_LIMIT=30;

        const onlineUsersWidget = document.getElementById('onlineUsersWidget'), onlineCount = document.getElementById('onlineCount'), onlineTooltip = document.getElementById('onlineTooltip');
        const studyTimerDisplay = document.getElementById('studyTimerDisplay'), timerText = document.getElementById('timerText');

        async function initPresence() {
            const { data: { user } } = await supabaseClient.auth.getUser();
            if (!user) return;
            currentUsername = user.user_metadata?.display_username || user.email?.split('@')[0] || 'کاربر';
            await updatePresence(false);
            if (presenceInterval) clearInterval(presenceInterval); presenceInterval = setInterval(sendHeartbeat, 20000);
            if (onlineUsersInterval) clearInterval(onlineUsersInterval); onlineUsersInterval = setInterval(fetchOnlineUsers, 10000);
            fetchOnlineUsers();
        }

        function stopPresence() { if (presenceInterval) clearInterval(presenceInterval); if (onlineUsersInterval) clearInterval(onlineUsersInterval); presenceInterval=onlineUsersInterval=null; }

        async function removePresence() { const { data: { user } } = await supabaseClient.auth.getUser(); if (user) await supabaseClient.from('user_presence').delete().eq('user_id', user.id); }

        // Heartbeat سمت سرور – از تابع update_heartbeat استفاده می‌کند
        async function sendHeartbeat() {
            const { data: { user } } = await supabaseClient.auth.getUser();
            if (!user) return;
            await supabaseClient.rpc('update_heartbeat', {
                p_user_id: user.id,
                p_username: currentUsername,
                p_is_studying: false  // heartbeat وضعیت مطالعه را تغییر نمی‌دهد
            });
        }

        async function updatePresence(isStudying) {
            const { data: { user } } = await supabaseClient.auth.getUser();
            if (!user) return;
            await supabaseClient.rpc('update_heartbeat', {
                p_user_id: user.id,
                p_username: currentUsername,
                p_is_studying: isStudying
            });
        }

        async function fetchOnlineUsers() {
            try {
                const { data, error } = await supabaseClient
                    .from('online_users_view')
                    .select('*');
                if (error) { console.error('خطا در دریافت کاربران:', error); onlineCount.textContent = '!'; return; }
                renderOnlineUsers(data || []);
            } catch (e) {
                console.error('fetchOnlineUsers error:', e);
                onlineCount.textContent = '?';
            }
        }

        function renderOnlineUsers(users) {
            onlineCount.textContent = users.length;
            if (users.length === 0) onlineTooltip.innerHTML = '<div style="text-align:center;color:var(--ink-soft);padding:8px;">کاربر آنلاینی وجود ندارد</div>';
            else {
                let html = '';
                users.forEach(u => {
                    const cls = u.is_studying ? ' studying' : '';
                    const label = u.is_studying ? ' 📖 در حال مطالعه' : '';
                    html += `<div class="user-row"><span class="user-status-dot${cls}"></span><span>${escapeHtml(u.username||'ناشناس')}${label}</span></div>`;
                });
                onlineTooltip.innerHTML = html;
            }
        }
        function escapeHtml(t) { const d=document.createElement('div'); d.textContent=t; return d.innerHTML; }

        function startStudyTimer() {
            sessionTotalSeconds=0; unsyncedSeconds=0; isStudyActive=true; lastActivityTime=Date.now(); updateTimerDisplay(); studyTimerDisplay.classList.remove('inactive');
            if (studyTimerInterval) clearInterval(studyTimerInterval);
            studyTimerInterval = setInterval(() => {
                const idleSec = Math.floor((Date.now()-lastActivityTime)/1000);
                if (idleSec >= INACTIVITY_LIMIT) { if (isStudyActive) { isStudyActive=false; studyTimerDisplay.classList.add('inactive'); updatePresence(false); } }
                else {
                    if (!isStudyActive) { isStudyActive=true; studyTimerDisplay.classList.remove('inactive'); updatePresence(true); }
                    sessionTotalSeconds++; unsyncedSeconds++; updateTimerDisplay();
                    if (unsyncedSeconds % 5 === 0) syncStudyTime();
                }
            }, 1000);
            setupActivityListeners();
        }

        function stopStudyTimer() { if (studyTimerInterval) { clearInterval(studyTimerInterval); studyTimerInterval=null; } removeActivityListeners(); syncStudyTime(); studyTimerDisplay.classList.add('inactive'); }

        async function syncStudyTime() {
            const { data: { user } } = await supabaseClient.auth.getUser();
            if (!user || unsyncedSeconds===0) return;
            const { data: existing } = await supabaseClient.from('user_presence').select('accumulated_study_seconds').eq('user_id', user.id).single();
            const previous = existing?.accumulated_study_seconds || 0;
            await supabaseClient.from('user_presence').upsert({ user_id: user.id, username: currentUsername, accumulated_study_seconds: previous + unsyncedSeconds }, { onConflict: 'user_id' });
            unsyncedSeconds=0;
        }

        function updateTimerDisplay() { const m=Math.floor(sessionTotalSeconds/60), s=sessionTotalSeconds%60; timerText.textContent = `${String(m).padStart(2,'0')}:${String(s).padStart(2,'0')}`; }

        function setupActivityListeners() { removeActivityListeners(); window.addEventListener('mousemove',activityHandler); window.addEventListener('keydown',activityHandler); window.addEventListener('click',activityHandler); window.addEventListener('touchstart',activityHandler); window.addEventListener('scroll',activityHandler); }
        function removeActivityListeners() { window.removeEventListener('mousemove',activityHandler); window.removeEventListener('keydown',activityHandler); window.removeEventListener('click',activityHandler); window.removeEventListener('touchstart',activityHandler); window.removeEventListener('scroll',activityHandler); }
        function activityHandler() { lastActivityTime=Date.now(); }

        window.addEventListener('beforeunload', () => { if (studyTimerInterval) syncStudyTime(); sendHeartbeat(); });

